    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        var bag = Bag.getInstance();
        var destinations = bag.showDestinations();
        
        String[] headers = {"Destino"};
        var model = new Object[destinations.length][1];
        
        for(var i = 0; i < destinations.length; i++) {
            model[i][0] = ((Flight) destinations[i]).getFlightDestination();
        }
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(model, headers));
    }//GEN-LAST:event_jButton1ActionPerformed

